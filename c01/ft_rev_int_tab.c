void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	t;
	int	f;

	i = 0;
	f = size - 1;
	while (i < size / 2)
	{
		t = tab[i];
		tab[i] = tab[f];
		tab[f] = t;
		i++;
		f--;
	}
}
