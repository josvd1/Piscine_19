int	ft_str_is_numeric(char *str)
{
	int	a;
	int	i;

	i = 0;
	a = 1;
	while ((str[i]) && (a == 1))
	{
		if ((str[i] < '0') || (str[i] > '9'))
		{
			a = 0;
		}
		i++;
	}	
	return (a);
}
