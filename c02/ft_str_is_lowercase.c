int	ft_str_is_lowercase(char *str)
{
	int	a;
	int	i;

	i = 0;
	a = 1;
	while ((str[i]) && (a == 1))
	{
		if ((str[i] < 'a') || (str[i] > 'z'))
		{
			a = 0;
		}
		i++;
	}
	return (a);
}
