char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int	i;
	int	s;

	i = 0;
	s = 0;
	while (dest[i])
	{
		i++;
	}
	while (0 < nb && src[s] != '\0')
	{
		dest[i + s] = src[s];
		s++;
		nb--;
	}
	dest[i + s] = '\0';
	return (dest);
}
