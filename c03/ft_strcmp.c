int	ft_strcmp(char *s1, char *s2)
{
	int	a;

	a = 0;
	while ((*s1 != '\0') && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	if ((*s1 > *s2) || (*s1 < *s2))
	{
		a = *s1 - *s2;
	}
	return (a);
}
